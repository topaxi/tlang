#[derive(Debug, Clone, PartialEq)]
pub(crate) struct FunctionContext {
    pub name: String,
    pub parameter_bindings: Vec<String>,
    pub is_tail_recursive: bool,
}

#[derive(Debug)]
pub struct CodegenJS {
    output: String,
    js_ast_json: String,
    source_map: Option<SourceMap>,
    protocol_names: HashSet<String>,
    /// When true, suppress `export` keywords on public declarations.
    /// Used when generating a bundled multi-module output.
    bundle_mode: bool,
}

impl Default for CodegenJS {
    fn default() -> Self {
        Self::new()
    }
}

impl CodegenJS {
    pub fn new() -> Self {
        let protocol_names = Self::get_standard_library_symbols()
            .iter()
            .filter(|(_, ty)| matches!(ty, DefKind::Protocol))
            .map(|(name, _)| (*name).to_string())
            .collect();

        Self {
            output: String::new(),
            js_ast_json: String::new(),
            source_map: None,
            protocol_names,
            bundle_mode: false,
        }
    }

    /// Returns the JS identifier for a protocol name, adding a `$` prefix
    /// to avoid collision with native JS globals (e.g. `Iterator`, `Iterable`).
    pub(crate) fn protocol_js_name(name: &str) -> String {
        format!("${name}")
    }

    /// Returns the tlang standard library source code, concatenating all
    /// embedded `.tlang` files in the correct order.
    pub fn get_standard_library_source() -> String {
        let notice = "// This file was auto-generated by the tlang compiler.";
        let parts: &[&str] = &Self::get_standard_library_tlang_files()
            .iter()
            .map(|(_, content)| *content)
            .collect::<Vec<_>>();
        format!("{notice}\n{}", parts.join("\n"))
    }

    /// Compiles the stdlib `.tlang` sources to JS and concatenates the native JS
    /// files (with `import` declarations stripped), producing a complete
    /// standalone stdlib bundle.
    ///
    /// The bundling order follows the stdlib file naming convention:
    /// 1. `*.pre.js` — runtime primitives needed before compiled tlang
    /// 2. Compiled `*.tlang` — the tlang standard library
    /// 3. `*.js` — native JS extensions that depend on compiled tlang symbols
    ///
    /// This performs a full parse → semantic analysis → HIR lowering → codegen
    /// pipeline at runtime, so callers that need the result repeatedly should
    /// cache it (e.g. via `std::sync::OnceLock`).
    ///
    /// # Panics
    ///
    /// Panics if the embedded stdlib `.tlang` sources fail to parse, fail
    /// semantic analysis, fail to lower to HIR, or fail to generate JavaScript
    /// successfully — any of which would indicate a bug in the stdlib source or
    /// the compiler.
    pub fn compile_stdlib_module() -> String {
        use tlang_ast_lowering::lower_to_hir;
        use tlang_hir_opt::hir_opt::HirOptContext;
        use tlang_semantics::SemanticAnalyzer;

        let source = Self::get_standard_library_source();

        let mut parser = tlang_parser::Parser::from_source(&source);
        let (mut ast, parse_meta) = parser
            .parse()
            .expect("stdlib .tlang source should parse without errors");

        let mut semantic_analyzer = SemanticAnalyzer::default();
        semantic_analyzer.add_builtin_symbols(Self::get_standard_library_symbols());
        semantic_analyzer
            .analyze(&mut ast)
            .expect("stdlib .tlang source should pass semantic analysis");

        let (mut module, meta) = lower_to_hir(
            &ast,
            &parse_meta.constant_pool_node_ids,
            semantic_analyzer.symbol_id_allocator(),
            semantic_analyzer.root_symbol_table(),
            semantic_analyzer.symbol_tables().clone(),
        )
        .expect("stdlib lowering should succeed");
        let mut ctx: HirOptContext = meta.into();

        let mut optimizer = crate::js_hir_opt::JsHirOptimizer::default();
        optimizer
            .optimize_hir(&mut module, &mut ctx)
            .expect("internal compiler error: stdlib HIR optimizer failed to converge");

        let mut generator = Self::new();
        // Bundle mode: suppress `export` on public declarations so the compiled
        // tlang code is script-compatible (no ES-module syntax).
        generator.set_bundle_mode(true);
        generator
            .generate_code(&module)
            .expect("stdlib codegen should succeed");
        let compiled_tlang = generator.get_output().to_string();

        let mut bundle = String::new();

        // Phase 1: *.pre.js — runtime primitives
        for (filename, content) in Self::get_standard_library_pre_js_files() {
            bundle.push_str("// -- ");
            bundle.push_str(filename);
            bundle.push_str(" --\n");
            bundle.push_str(&strip_module_declarations(content));
        }

        // Phase 2: compiled *.tlang
        bundle.push_str(&compiled_tlang);

        // Phase 3: *.js — native extensions
        for (filename, content) in Self::get_standard_library_post_js_files() {
            bundle.push('\n');
            bundle.push_str("// -- ");
            bundle.push_str(filename);
            bundle.push_str(" --\n");
            bundle.push_str(&strip_module_declarations(content));
        }

        bundle
    }

    /// Returns all native JavaScript code (pre + post) concatenated.
    /// Used by WASM bindings for non-bundled mode.
    pub fn get_standard_library_native_js() -> String {
        Self::get_standard_library_native_js_files()
            .iter()
            .map(|(_, content)| *content)
            .collect::<Vec<_>>()
            .join("\n")
    }

    /// Returns all native JS stdlib files as `(filename, content)`, combining
    /// both pre and post phases.  Used by `cargo xtask gen-stdlib` and tests.
    pub fn get_standard_library_native_js_files() -> Vec<(&'static str, &'static str)> {
        Self::get_standard_library_pre_js_files()
            .iter()
            .chain(Self::get_standard_library_post_js_files().iter())
            .copied()
            .collect()
    }

    /// Returns the embedded `.tlang` stdlib source files as `(filename, content)`.
    pub fn get_standard_library_tlang_files() -> &'static [(&'static str, &'static str)] {
        &[
            ("string.tlang", include_str!("../std/string.tlang")),
            ("option.tlang", include_str!("../std/option.tlang")),
            ("result.tlang", include_str!("../std/result.tlang")),
            ("protocols.tlang", include_str!("../std/protocols.tlang")),
            ("math.tlang", include_str!("../std/math.tlang")),
            (
                "collections.tlang",
                include_str!("../std/collections.tlang"),
            ),
        ]
    }

    /// Returns `*.pre.js` files — runtime primitives that must load *before*
    /// the compiled `.tlang` code (e.g. `$Protocol`, `$impl`, `$uncurryThis`).
    pub fn get_standard_library_pre_js_files() -> &'static [(&'static str, &'static str)] {
        &[("globals.pre.js", include_str!("../std/globals.pre.js"))]
    }

    /// Returns `*.js` files — native extensions that load *after* the compiled
    /// `.tlang` code and may reference symbols it defines (e.g. `$Display`).
    pub fn get_standard_library_post_js_files() -> &'static [(&'static str, &'static str)] {
        &[
            ("collections.js", include_str!("../std/collections.js")),
            ("string.js", include_str!("../std/string.js")),
            ("regex.js", include_str!("../std/regex.js")),
            ("string_buf.js", include_str!("../std/string_buf.js")),
            ("temporal.js", include_str!("../std/temporal.js")),
        ]
    }

    pub fn get_standard_library_symbols() -> &'static [(&'static str, DefKind)] {
        /// Symbols that exist in the semantic layer only — no JS name mapping.
        static SEMANTIC_ONLY_SYMBOLS: &[(&str, DefKind)] = &[
            ("Option", DefKind::Enum),
            ("Result", DefKind::Enum),
            ("Option::Some", DefKind::EnumVariant(1)),
            ("Option::None", DefKind::EnumVariant(0)),
            ("Result::Ok", DefKind::EnumVariant(1)),
            ("Result::Err", DefKind::EnumVariant(1)),
            ("Functor", DefKind::Protocol),
            ("Functor::map", DefKind::ProtocolMethod(2)),
            ("Match", DefKind::Protocol),
            ("Match::matches", DefKind::ProtocolMethod(2)),
            ("Regex", DefKind::Struct),
            ("Iterable", DefKind::Protocol),
            ("Iterable::iter", DefKind::ProtocolMethod(1)),
            ("Iterator", DefKind::Protocol),
            ("Iterator::next", DefKind::ProtocolMethod(1)),
            ("Display", DefKind::Protocol),
            ("Display::to_string", DefKind::ProtocolMethod(1)),
            ("len", DefKind::Function(1)),
            ("compose", DefKind::Function(2)),
            ("re", DefKind::Function(2)),
            ("f", DefKind::Function(2)),
            ("map", DefKind::Function(2)),
            ("panic", DefKind::Function(1)),
            ("string", DefKind::Module),
        ];

        static SYMBOLS: LazyLock<Vec<(&'static str, DefKind)>> = LazyLock::new(|| {
            JS_BUILTINS
                .iter()
                .filter_map(|b| b.def_kind.map(|dk| (b.tlang_name, dk)))
                .chain(SEMANTIC_ONLY_SYMBOLS.iter().copied())
                .collect()
        });

        &SYMBOLS
    }

    pub fn generate_code(&mut self, module: &hir::Module) -> Result<(), Vec<CodegenError>> {
        self.run_codegen(module, None, None)
    }

    /// Enable bundle mode: suppresses `export` keywords on public declarations.
    pub fn set_bundle_mode(&mut self, bundle: bool) {
        self.bundle_mode = bundle;
    }

    /// Register a user-defined protocol name so the codegen emits `$ProtocolName`
    /// instead of `ProtocolName` when generating path expressions.
    pub fn register_protocol(&mut self, name: &str) {
        self.protocol_names.insert(name.to_string());
    }

    /// Generate code and produce a source map mapping generated JS back to the
    /// original tlang source. `source_name` is stored as the source file name in
    /// the map; `source_text` is the original tlang source used to build it.
    pub fn generate_code_with_source_map(
        &mut self,
        module: &hir::Module,
        source_name: &str,
        source_text: &str,
    ) -> Result<(), Vec<CodegenError>> {
        self.run_codegen(module, Some(source_text), Some(source_name))
    }

    fn run_codegen(
        &mut self,
        module: &hir::Module,
        source_text: Option<&str>,
        source_name: Option<&str>,
    ) -> Result<(), Vec<CodegenError>> {
        let allocator = Allocator::default();
        let ast = AstBuilder::new(&allocator);
        let mut inner = InnerCodegen::new(ast, self.protocol_names.clone());
        inner.bundle_mode = self.bundle_mode;
        if let Some(st) = source_text {
            inner = inner.with_source_text(st.to_string());
        }
        let mut program = inner.generate_module(module);

        if !inner.errors.is_empty() {
            // Clear any stale output from a previous successful run so that
            // callers cannot accidentally use old output after an error.
            self.output.clear();
            self.js_ast_json.clear();
            self.source_map = None;
            return Err(inner.errors);
        }

        // Post-process: simplify `if (c) { return true } else { return false }`
        // into `return c` (or `return !c` when swapped).
        let post_ast = AstBuilder::new(&allocator);
        crate::js_boolean_return_simplification::simplify_boolean_returns(
            &mut program,
            &post_ast,
        );

        let mut serializer = PrettyJSSerializer::new(false);
        program.serialize(&mut serializer);
        self.js_ast_json = serializer.into_string();

        let mut options = CodegenOptions {
            indent_char: IndentChar::Space,
            indent_width: 4,
            ..CodegenOptions::default()
        };
        if let Some(name) = source_name {
            options.source_map_path = Some(std::path::PathBuf::from(name));
        }

        let result = Codegen::new().with_options(options).build(&program);
        self.output = result.code;
        self.source_map = result.map;
        Ok(())
    }

    pub fn get_output(&self) -> &str {
        &self.output
    }

    pub fn get_js_ast_json(&self) -> &str {
        &self.js_ast_json
    }

    pub fn get_source_map(&self) -> Option<&SourceMap> {
        self.source_map.as_ref()
    }

    /// Returns the source map serialized as a JSON string, or `None` if no
    /// source map was generated. The generated line positions in the map
    /// correspond to `get_output()` — call [`shift_source_map_lines`] if you
    /// need to account for lines prepended to the final file (e.g. stdlib).
    pub fn get_source_map_json(&self) -> Option<String> {
        self.source_map.as_ref().map(|m| m.to_json_string())
    }
}

/// Shift every generated mapping in `map_json` down by `line_offset` lines.
///
/// This is used when the compiled output is prefixed with a preamble such as
/// the stdlib, so generated JS line numbers still point back to the original
/// tlang source locations.
///
/// # Panics
///
/// Panics if `map_json` is not a valid source map JSON string.
pub fn shift_source_map_lines(map_json: &str, line_offset: usize) -> String {
    if line_offset == 0 {
        return map_json.to_string();
    }

    let source_map =
        SourceMap::from_json_string(map_json).expect("generated source map should be valid JSON");
    let mut shifted = SourceMap::new(
        source_map.get_file().cloned(),
        source_map.get_names().cloned().collect(),
        source_map.get_source_root().map(str::to_owned),
        source_map.get_sources().cloned().collect(),
        source_map
            .get_source_contents()
            .map(|content| content.cloned())
            .collect(),
        source_map
            .get_tokens()
            .map(|token| {
                Token::new(
                    token.get_dst_line() + line_offset as u32,
                    token.get_dst_col(),
                    token.get_src_line(),
                    token.get_src_col(),
                    token.get_source_id(),
                    token.get_name_id(),
                )
            })
            .collect::<Vec<_>>()
            .into_boxed_slice(),
        None,
    );

    if let Some(ignore_list) = source_map.get_x_google_ignore_list() {
        shifted.set_x_google_ignore_list(ignore_list.to_vec());
    }

    if let Some(debug_id) = source_map.get_debug_id() {
        shifted.set_debug_id(debug_id);
    }

    shifted.to_json_string()
}

/// Transform a native JS ES-module source into a script-compatible form by
/// removing `import` declarations and stripping the `export` keyword from
/// named exports.  Import statements are removed entirely; `export function f`
/// becomes `function f`, `export const x` becomes `const x`, and so on.
///
/// The transformation is driven by the OXC parser so that only genuine module
/// declarations are touched — identical text inside comments or string literals
/// is left alone.  If OXC reports parse errors the source is returned unchanged
/// rather than silently producing output from a partial AST.
fn strip_module_declarations(source: &str) -> String {
    use oxc_parser::Parser;
    use oxc_span::{GetSpan, SourceType};

    let allocator = Allocator::default();
    let ret = Parser::new(&allocator, source, SourceType::mjs()).parse();

    // If parsing failed, fall back to the original source unchanged.
    if !ret.errors.is_empty() {
        return source.to_string();
    }

    // Collect byte ranges to exclude from the output (in source order).
    let mut exclude: Vec<(usize, usize)> = Vec::new();

    // Helper: advance `end` past a trailing newline (`\n` or `\r\n`) so that
    // removing a full-line statement doesn't leave a blank line behind.
    let consume_trailing_newline = |mut end: usize| -> usize {
        let bytes = source.as_bytes();
        if bytes.get(end) == Some(&b'\r') && bytes.get(end + 1) == Some(&b'\n') {
            end += 2;
        } else if bytes.get(end) == Some(&b'\n') {
            end += 1;
        }
        end
    };

    for stmt in &ret.program.body {
        match stmt {
            Statement::ImportDeclaration(decl) => {
                // Exclude the entire import declaration.
                let start = decl.span.start as usize;
                let end = consume_trailing_newline(decl.span.end as usize);
                exclude.push((start, end));
            }
            Statement::ExportNamedDeclaration(decl) => {
                if let Some(inner_decl) = &decl.declaration {
                    // Strip only the `export ` prefix; keep the inner declaration.
                    exclude.push((decl.span.start as usize, inner_decl.span().start as usize));
                } else {
                    // Re-export with no inner declaration (e.g. `export { foo } from 'bar'`):
                    // exclude the whole statement.
                    exclude.push((decl.span.start as usize, decl.span.end as usize));
                }
            }
            Statement::ExportDefaultDeclaration(decl) => {
                use oxc_ast::ast::ExportDefaultDeclarationKind;

                match &decl.declaration {
                    // Anonymous default function/class declarations would become invalid
                    // statements (`function() {}` / `class {}`) if we stripped only the
                    // `export default ` prefix. For these, exclude the entire statement,
                    // including a trailing newline if present.
                    ExportDefaultDeclarationKind::FunctionDeclaration(func)
                        if func.id.is_none() =>
                    {
                        exclude.push((
                            decl.span.start as usize,
                            consume_trailing_newline(decl.span.end as usize),
                        ));
                    }
                    ExportDefaultDeclarationKind::ClassDeclaration(class) if class.id.is_none() => {
                        exclude.push((
                            decl.span.start as usize,
                            consume_trailing_newline(decl.span.end as usize),
                        ));
                    }
                    // For named defaults and expression defaults, keep the inner
                    // value/declaration and strip only `export default `.
                    _ => {
                        let inner_start = decl.declaration.span().start as usize;
                        exclude.push((decl.span.start as usize, inner_start));
                    }
                }
            }
            _ => {}
        }
    }

    if exclude.is_empty() {
        return source.to_string();
    }

    // Reconstruct the output by copying everything except the excluded ranges.
    let bytes = source.as_bytes();
    let mut out = Vec::with_capacity(source.len());
    let mut pos = 0usize;

    for (start, end) in exclude {
        out.extend_from_slice(&bytes[pos..start]);
        pos = end;
    }
    out.extend_from_slice(&bytes[pos..]);

    String::from_utf8(out).unwrap_or_else(|_| source.to_string())
}
