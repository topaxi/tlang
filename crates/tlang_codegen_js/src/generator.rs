// InnerCodegen: arena-scoped codegen that builds an OXC AST
// ---------------------------------------------------------------------------

pub(crate) struct InnerCodegen<'a> {
    pub ast: AstBuilder<'a>,
    pub name_map: NameMap,
    pub function_context_stack: Vec<FunctionContext>,
    pub protocol_names: HashSet<String>,
    /// When true, suppress `export` keywords on public declarations.
    pub bundle_mode: bool,
    /// Original tlang source text. When non-empty, it's prepended to
    /// `comment_source` as the combined `Program.source_text`, enabling OXC's
    /// built-in source map generation (which reads byte offsets from node spans).
    source_text: String,
    /// Buffer of synthetic source text containing comment strings.
    /// OXC codegen reads comment content from Program.source_text using byte spans.
    comment_source: String,
    /// Collected OXC comments to be attached to the Program.
    oxc_comments: Vec<oxc_ast::Comment>,
    /// Counter for assigning unique span start positions to comment-bearing
    /// statement nodes, ensuring comment attachment keys are always distinct
    /// (even when multiple HIR nodes derive from the same source position).
    span_counter: u32,
    /// Errors accumulated during code generation (e.g. unsupported features).
    /// Checked at the end of [`generate_module`]; if non-empty the caller
    /// should return an error instead of emitting output.
    pub(crate) errors: Vec<CodegenError>,
}

impl<'a> InnerCodegen<'a> {
    pub fn new(ast: AstBuilder<'a>, protocol_names: HashSet<String>) -> Self {
        Self {
            ast,
            name_map: NameMap::new(),
            function_context_stack: Vec::new(),
            protocol_names,
            bundle_mode: false,
            source_text: String::new(),
            comment_source: String::new(),
            oxc_comments: Vec::new(),
            span_counter: 1,
            errors: Vec::new(),
        }
    }

    pub fn with_source_text(mut self, source_text: String) -> Self {
        self.source_text = source_text;
        self
    }

    /// Record an unsupported-feature error and return a fallback `undefined`
    /// expression so that code generation can continue and collect further
    /// errors before reporting.
    pub(crate) fn unsupported_expr(&mut self, feature: &str, span: TlangSpan) -> Expression<'a> {
        self.errors.push(CodegenError::unsupported(feature, span));
        self.undefined_expr()
    }

    /// Record an unresolved-identifier error and return a fallback `undefined`
    /// expression so that code generation can continue and collect further
    /// errors before reporting.
    pub(crate) fn unresolved_identifier_expr(
        &mut self,
        name: &str,
        span: TlangSpan,
    ) -> Expression<'a> {
        self.errors
            .push(CodegenError::unresolved_identifier(name, span));
        self.undefined_expr()
    }

    pub fn generate_module(&mut self, module: &hir::Module) -> Program<'a> {
        let stmts = self.generate_stmts(&module.block.stmts);
        let body = self.ast.vec_from_iter(stmts);
        // Combine the original tlang source (for source map byte-offset lookups)
        // with the synthetic comment buffer (for OXC's comment printer).
        let combined = format!("{}{}", self.source_text, self.comment_source);
        let source_text = self.ast.allocator.alloc_str(&combined);
        let comments = self.ast.vec_from_iter(self.oxc_comments.drain(..));
        self.ast.program(
            SPAN,
            SourceType::mjs(),
            source_text,
            comments,
            None,
            self.ast.vec(),
            body,
        )
    }

    /// Convert a tlang span to an OXC span using byte offsets.
    pub fn hir_span(span: TlangSpan) -> oxc_span::Span {
        oxc_span::Span::new(span.start, span.end)
    }

    // -- Scope helpers -------------------------------------------------------

    pub fn push_scope(&mut self) {
        self.name_map.push_scope();
    }

    pub fn pop_scope(&mut self) {
        self.name_map.pop_scope();
    }

    // -- Function context helpers --------------------------------------------

    pub fn get_function_context(&self) -> Option<&FunctionContext> {
        self.function_context_stack.last()
    }

    pub fn push_function_context(
        &mut self,
        name: &str,
        parameters: &[hir::FunctionParameter],
        parameter_bindings: &[String],
        is_tail_recursive: bool,
    ) {
        let parameter_bindings = if !is_tail_recursive {
            vec![]
        } else if parameters.len() == parameter_bindings.len() {
            parameter_bindings.to_vec()
        } else {
            parameters
                .iter()
                .map(|param| {
                    if param.name.is_wildcard() {
                        self.name_map.alloc_tmp()
                    } else {
                        param.name.to_string()
                    }
                })
                .collect()
        };

        self.function_context_stack.push(FunctionContext {
            name: name.to_string(),
            parameter_bindings,
            is_tail_recursive,
        });
    }

    pub fn pop_function_context(&mut self) {
        self.function_context_stack.pop();
    }

    // -- Protocol helpers ----------------------------------------------------

    pub fn is_protocol(&self, name: &str) -> bool {
        self.protocol_names.contains(name)
    }

    pub fn register_protocol(&mut self, name: &str) {
        self.protocol_names.insert(name.to_string());
    }

    // -- Comment helpers -------------------------------------------------------

    /// Allocate a unique span start for a statement node.
    /// OXC's codegen uses `span.start` to look up attached comments.
    fn next_node_start(&mut self) -> u32 {
        let id = self.span_counter;
        self.span_counter += 1;
        id
    }

    /// Attach HIR leading comments to an OXC statement. A unique counter-based
    /// ID is used as the attachment key so that nodes derived from the same
    /// source position (e.g., a function clause and its pattern-dispatch branch)
    /// don't accidentally share a `span.start` and trigger duplicate comments.
    /// Comment text is appended to `comment_source`, placed after the original
    /// source in the combined `Program.source_text`.
    pub fn attach_leading_comments(
        &mut self,
        stmt: &mut Statement<'a>,
        comments: &[tlang_ast::token::CommentToken],
    ) {
        use oxc_ast::{Comment, CommentKind, CommentPosition};
        use oxc_span::GetSpanMut;

        if comments.is_empty() {
            return;
        }

        let node_start = self.next_node_start();
        let span = stmt.span_mut();
        span.start = node_start;
        if span.end < node_start {
            span.end = node_start;
        }

        for comment_token in comments {
            let (text, oxc_kind) = match comment_token.kind {
                tlang_ast::token::CommentKind::SingleLine => {
                    (format!("//{}", comment_token.text), CommentKind::Line)
                }
                tlang_ast::token::CommentKind::MultiLine => (
                    format!("/*{}*/", comment_token.text),
                    CommentKind::MultiLineBlock,
                ),
            };

            // Offset the comment span past the original source text so that the
            // combined Program.source_text (original + comment_source) is indexed
            // correctly by both the source map builder and the comment printer.
            let start = self.source_text.len() as u32 + self.comment_source.len() as u32;
            self.comment_source.push_str(&text);
            self.comment_source.push('\n');
            let end = start + text.len() as u32;

            self.oxc_comments.push(Comment {
                span: oxc_span::Span::new(start, end),
                attached_to: node_start,
                kind: oxc_kind,
                position: CommentPosition::Leading,
                newlines: CommentNewlines::Leading | CommentNewlines::Trailing,
                content: oxc_ast::CommentContent::None,
            });
        }
    }

    // -- AST building helpers ------------------------------------------------

    /// Allocate a string into the arena, returning a reference with lifetime `'a`.
    pub fn alloc_str(&self, s: &str) -> &'a str {
        self.ast.allocator.alloc_str(s)
    }

    pub fn ident_expr(&self, name: &str) -> Expression<'a> {
        self.ast.expression_identifier(SPAN, self.alloc_str(name))
    }

    pub fn ident_name(&self, name: &str) -> IdentifierName<'a> {
        self.ast.identifier_name(SPAN, self.alloc_str(name))
    }

    pub fn binding_ident(&self, name: &str) -> BindingIdentifier<'a> {
        self.ast.binding_identifier(SPAN, self.alloc_str(name))
    }

    pub fn num_expr(&self, value: f64) -> Expression<'a> {
        self.ast
            .expression_numeric_literal(SPAN, value, None, NumberBase::Decimal)
    }

    pub fn str_expr(&self, value: &str) -> Expression<'a> {
        self.ast
            .expression_string_literal(SPAN, self.alloc_str(value), None)
    }

    pub fn bool_expr(&self, value: bool) -> Expression<'a> {
        self.ast.expression_boolean_literal(SPAN, value)
    }

    pub fn undefined_expr(&self) -> Expression<'a> {
        self.ident_expr("undefined")
    }

    pub fn this_expr(&self) -> Expression<'a> {
        self.ast.expression_this(SPAN)
    }

    pub fn static_member_expr(&self, object: Expression<'a>, property: &str) -> Expression<'a> {
        Expression::StaticMemberExpression(self.ast.alloc_static_member_expression(
            SPAN,
            object,
            self.ident_name(property),
            false,
        ))
    }

    pub fn computed_member_expr(
        &self,
        object: Expression<'a>,
        index: Expression<'a>,
    ) -> Expression<'a> {
        Expression::ComputedMemberExpression(
            self.ast
                .alloc_computed_member_expression(SPAN, object, index, false),
        )
    }

    pub fn assignment_target_ident(&self, name: &str) -> AssignmentTarget<'a> {
        AssignmentTarget::from(
            self.ast
                .simple_assignment_target_assignment_target_identifier(SPAN, self.alloc_str(name)),
        )
    }

    pub fn assignment_target_member(
        &self,
        object: Expression<'a>,
        property: &str,
    ) -> AssignmentTarget<'a> {
        AssignmentTarget::from(SimpleAssignmentTarget::StaticMemberExpression(
            self.ast
                .alloc_static_member_expression(SPAN, object, self.ident_name(property), false),
        ))
    }

    pub fn assignment_target_computed(
        &self,
        object: Expression<'a>,
        index: Expression<'a>,
    ) -> AssignmentTarget<'a> {
        AssignmentTarget::from(SimpleAssignmentTarget::ComputedMemberExpression(
            self.ast
                .alloc_computed_member_expression(SPAN, object, index, false),
        ))
    }

    pub fn assign_expr(
        &self,
        target: AssignmentTarget<'a>,
        value: Expression<'a>,
    ) -> Expression<'a> {
        self.ast
            .expression_assignment(SPAN, AssignmentOperator::Assign, target, value)
    }

    pub fn binding_pattern_ident(&self, name: &str) -> BindingPattern<'a> {
        self.ast
            .binding_pattern_binding_identifier(SPAN, self.alloc_str(name))
    }

    pub fn formal_param(&self, name: &str) -> FormalParameter<'a> {
        self.ast.formal_parameter(
            SPAN,
            self.ast.vec(),
            self.binding_pattern_ident(name),
            NONE,
            NONE,
            false,
            None,
            false,
            false,
        )
    }

    pub fn formal_params(&self, items: OxcVec<'a, FormalParameter<'a>>) -> FormalParameters<'a> {
        self.ast
            .formal_parameters(SPAN, FormalParameterKind::FormalParameter, items, NONE)
    }

    pub fn fn_body(&self, stmts: OxcVec<'a, Statement<'a>>) -> FunctionBody<'a> {
        self.ast.function_body(SPAN, self.ast.vec(), stmts)
    }

    /// Create a `let` variable declaration statement with optional initializer.
    pub fn let_decl(&self, name: &str, init: Option<Expression<'a>>) -> Statement<'a> {
        let declarator = self.ast.variable_declarator(
            SPAN,
            VariableDeclarationKind::Let,
            self.binding_pattern_ident(name),
            NONE,
            init,
            false,
        );
        Statement::VariableDeclaration(self.ast.alloc_variable_declaration(
            SPAN,
            VariableDeclarationKind::Let,
            self.ast.vec1(declarator),
            false,
        ))
    }

    /// Create a `const` variable declaration statement.
    pub fn const_decl(&self, name: &str, init: Expression<'a>) -> Statement<'a> {
        let declarator = self.ast.variable_declarator(
            SPAN,
            VariableDeclarationKind::Const,
            self.binding_pattern_ident(name),
            NONE,
            Some(init),
            false,
        );
        Statement::VariableDeclaration(self.ast.alloc_variable_declaration(
            SPAN,
            VariableDeclarationKind::Const,
            self.ast.vec1(declarator),
            false,
        ))
    }

    /// Create a let destructuring declaration: `let [a, b, ...rest] = expr;`
    pub fn let_array_destructuring(
        &self,
        elements: OxcVec<'a, Option<BindingPattern<'a>>>,
        rest: Option<BindingPattern<'a>>,
        init: Expression<'a>,
    ) -> Statement<'a> {
        let rest_element = rest.map(|r| self.ast.alloc_binding_rest_element(SPAN, r));
        let pattern = self
            .ast
            .binding_pattern_array_pattern(SPAN, elements, rest_element);
        let declarator = self.ast.variable_declarator(
            SPAN,
            VariableDeclarationKind::Let,
            pattern,
            NONE,
            Some(init),
            false,
        );
        Statement::VariableDeclaration(self.ast.alloc_variable_declaration(
            SPAN,
            VariableDeclarationKind::Let,
            self.ast.vec1(declarator),
            false,
        ))
    }

    pub fn expr_stmt(&self, expr: Expression<'a>) -> Statement<'a> {
        self.ast.statement_expression(SPAN, expr)
    }

    pub fn block_stmt(&self, stmts: Vec<Statement<'a>>) -> Statement<'a> {
        self.ast
            .statement_block(SPAN, self.ast.vec_from_iter(stmts))
    }

    pub fn call_expr(&self, callee: Expression<'a>, args: Vec<Argument<'a>>) -> Expression<'a> {
        self.ast
            .expression_call(SPAN, callee, NONE, self.ast.vec_from_iter(args), false)
    }
}

#[cfg(test)]
mod tests {
    use super::shift_source_map_lines;
    use oxc_sourcemap::{SourceMap, Token};

    #[test]
    fn shift_source_map_lines_preserves_metadata_and_offsets_tokens() {
        let mut source_map = SourceMap::new(
            Some("out.js".into()),
            vec!["main".into()],
            Some("src".to_string()),
            vec!["input.tlang".into()],
            vec![Some("fn main() { 1 }".into())],
            vec![
                Token::new(0, 0, 0, 0, Some(0), Some(0)),
                Token::new(2, 4, 0, 3, Some(0), None),
            ]
            .into_boxed_slice(),
            None,
        );
        source_map.set_debug_id("debug-id");
        source_map.set_x_google_ignore_list(vec![0]);

        let shifted_json = shift_source_map_lines(&source_map.to_json_string(), 3);
        let shifted = SourceMap::from_json_string(&shifted_json).unwrap();
        let shifted_lines = shifted
            .get_tokens()
            .map(|token| token.get_dst_line())
            .collect::<Vec<_>>();

        assert_eq!(shifted_lines, vec![3, 5]);
        assert_eq!(shifted.get_file().map(|file| file.as_ref()), Some("out.js"));
        assert_eq!(shifted.get_source_root(), Some("src"));
        assert_eq!(shifted.get_debug_id(), Some("debug-id"));
        assert_eq!(shifted.get_x_google_ignore_list(), Some(&[0][..]));
    }
}
