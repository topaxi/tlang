use clap::{Parser, Subcommand};

#[derive(Parser)]
#[command(name = "xtask", about = "Development task runner for tlang")]
struct Cli {
    #[command(subcommand)]
    command: Command,
}

#[derive(Subcommand)]
enum Command {
    /// Compile .tlang stdlib and bundle with native JS
    GenStdlib,
}

fn main() {
    let cli = Cli::parse();

    match cli.command {
        Command::GenStdlib => gen_stdlib(),
    }
}

fn gen_stdlib() {
    let bundle = build_bundle();
    write_bundle(&bundle);
}

fn build_bundle() -> String {
    let mut bundle = String::new();
    bundle.push_str("// Auto-generated by `cargo xtask gen-stdlib`. Do not edit directly.\n");
    bundle.push_str("// Run `cargo xtask gen-stdlib` to regenerate.\n");
    bundle.push('\n');
    bundle.push_str(&tlang_codegen_js::generator::CodegenJS::compile_stdlib_module());
    bundle
}

fn write_bundle(bundle: &str) {
    let workspace_root = std::path::Path::new(env!("CARGO_MANIFEST_DIR"))
        .parent()
        .expect("xtask crate should have a parent directory");
    let out_path = workspace_root.join("crates/tlang_bindings_js/pkg/stdlib.js");

    std::fs::create_dir_all(out_path.parent().unwrap()).expect("failed to create pkg directory");
    std::fs::write(&out_path, bundle).expect("failed to write stdlib bundle");

    println!("Written {} bytes to {}", bundle.len(), out_path.display());
}
